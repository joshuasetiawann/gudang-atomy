-- ---- 3) Verifikasi --------------------------------------------------
-- target: BSTR-FZ 14 | BSTR-HSD 18 | BSTR-EOP 8 (termasuk 0005 Kantor Bunda)
--         BSTR-HEMO 44 aktif (0001-0044) | BLM-HEMO 240 | 0009-2 = void
select
  case
    when id_box like 'BSTR-FZ-%'   then 'BSTR-FZ'
    when id_box like 'BSTR-HSD-%'  then 'BSTR-HSD'
    when id_box like 'BSTR-EOP-%'  then 'BSTR-EOP'
    when id_box like 'BSTR-HEMO-%' then 'BSTR-HEMO'
    when id_box like 'BLM-HEMO-%'  then 'BLM-HEMO'
  end as prefix,
  count(*) filter (where b.status <> 'void')             as box_aktif,
  count(*) filter (where b.status = 'void')              as box_void,
  sum(bi.qty_initial) filter (where b.status <> 'void')  as total_pcs
from public.boxes b left join public.box_items bi on bi.box_id = b.id
where id_box like 'BSTR-FZ-%' or id_box like 'BSTR-HSD-%' or id_box like 'BSTR-EOP-%'
   or id_box like 'BSTR-HEMO-%' or id_box like 'BLM-HEMO-%'
group by 1 order by 1;
